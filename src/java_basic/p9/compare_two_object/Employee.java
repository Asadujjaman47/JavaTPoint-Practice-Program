package java_basic.p9.compare_two_object;

public class Employee {
    int regno;
    String name;

    public Employee(int regno, String name) {
        this.regno = regno;
        this.name = name;
    }

    public int getRegno() {
        return regno;
    }

    public void setRegno(int regno) {
        this.regno = regno;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
