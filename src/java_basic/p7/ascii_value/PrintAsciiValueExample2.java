package java_basic.p7.ascii_value;

// Print ASCII Value in Java
// Assigning a Variable to the int Variable
public class PrintAsciiValueExample2 {
    public static void main(String[] args) {
        int ch1 = 'a';
        int ch2 = 'b';

        System.out.println("The ASCII value of " + ch1 + " is: " + ch1);
        System.out.println("The ASCII value of " + ch2 + " is: " + ch2);
    }
}
